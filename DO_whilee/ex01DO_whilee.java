package DO_whilee;

public class ex01DO_whilee {
    public static void main(String[] args){
        int num = 1;
        System.out.println("vai de 1 a o 100");
        do {
            System.out.println("numero :"+ num);
            num++;
        }while (num <= 100);
        System.out.println("concluido 1 a 100");
    }
}
